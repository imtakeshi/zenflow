function getDayIndex(): number {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const day = Math.floor(diff / (1000 * 60 * 60 * 24));
  return day;
}

function getWeekIndex(): number {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 1);
  const diff = now.getTime() - start.getTime();
  const week = Math.floor(diff / (1000 * 60 * 60 * 24 * 7));
  return week;
}

function getMonthIndex(): number {
  const now = new Date();
  return now.getFullYear() * 12 + now.getMonth();
}

export function getAffirmation(period: "day" | "week" | "month"): string {
  let index: number;
  if (period === "day") index = getDayIndex();
  else if (period === "week") index = getWeekIndex();
  else index = getMonthIndex();
  return AFFIRMATIONS[Math.abs(index) % AFFIRMATIONS.length];
}
